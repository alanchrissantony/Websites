<script>
    // styles
    import '@/styles/about.scss';
    // ScrollMagic
    import * as ScrollMagic from "scrollmagic";

    export default {
        name: 'about',
        data() {
            return {
                intro: new TimelineMax(),
                scroller: new ScrollMagic.Controller(),
            }
        },
        methods: {},
        mounted() {

            /**
             * @desc
             * Intro scene
             */
            this.intro
                .addLabel('enter', 1)
                .from('.title', 2, {
                    autoAlpha: 0,
                    rotationX: 90,
                    transformOrigin: '50% 50% -100px',
                    ease: Power3.easeOut,
                }, 'enter')
                .from('.std', 2, {
                    autoAlpha: 0,
                    x: -32,
                    ease: Power3.easeOut,
                }, 'enter+=1.5');

            /**
             * @desc
             * Setup Time lines and Scenes
             */
            let duration = window.innerHeight;

            /**
             * @desc
             * header background scene
             */
            let tlHeader = new TimelineMax();

            tlHeader
                .to('.header-bg', 4, {
                    autoAlpha: 1,
                    ease: Power0.easeNone,
                });

            new ScrollMagic.Scene({
                triggerElement: '#about',
                offset: duration/4,
                duration: duration
            })
                .setTween(tlHeader)
                .addTo(this.scroller)
                .reverse(true);

        },
        beforeDestroy() {

            this.scroller.destroy();

        }
    };
</script>
